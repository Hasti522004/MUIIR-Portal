    <script>
        function toggleText(button, text) {
            var dots = text.querySelector(".dots");
            var moreText = text.querySelector(".more");

            if (dots.style.display === "none") {
                dots.style.display = "inline";
                button.innerHTML = "Read more";
                moreText.style.display = "none";
            } else {
                dots.style.display = "none";
                button.innerHTML = "Read less";
                moreText.style.display = "inline";
            }
        }

        document.querySelectorAll(".read-more-btn").forEach(function(button) {
            var text = button.previousElementSibling;

            // Add dots and hide extra text
            text.innerHTML = text.innerHTML + '<span class="dots">...</span><span class="more">' + text.dataset.more + '</span>';
            text.querySelector(".more").style.display = "none";

            // Add click listener to button
            button.addEventListener("click", function() {
                toggleText(button, text);
            });
        });
    </script>

